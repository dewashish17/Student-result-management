    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String  rollNO=jTextField1.getText();
        try{
            Connection con =ConnectionProvider.getcon();
            Statement st=con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs=st.executeQuery("select * from result where rollno='"+rollNO+"'");
           // jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered Roll no doesn't exist");
//                setVisible(false);
//                insertResult frame=new insertResult();
//                frame.setVisible(true);
            }
            else {
                jTextField1.setEditable(false);
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(3));        //  rs={1:rollno, 2:physics,3:maths,4:chem,5:electrical,6:biology}
                jTextField4.setText(rs.getString(4));
                jTextField5.setText(rs.getString(5));
                jTextField6.setText(rs.getString(6));
                
            }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String rollNo=jTextField1.getText();
        int physicsMarks=Integer.parseInt(jTextField2.getText());
        int mathsMarks=Integer.parseInt(jTextField3.getText());
        int chemMarks=Integer.parseInt(jTextField4.getText());
        int electricalMarks=Integer.parseInt(jTextField5.getText());
        int bioMarks=Integer.parseInt(jTextField6.getText());
         int total=physicsMarks+mathsMarks+chemMarks+electricalMarks+bioMarks;
         try{
            Connection con=ConnectionProvider.getcon();
            Statement st=con.createStatement();     // using this we will execute command into sql
            st.executeUpdate("update result set physics="+physicsMarks+",maths="+mathsMarks+",chemistry="+chemMarks+",electrical="+electricalMarks+",biology="+bioMarks+",total="+total+" where rollno='"+rollNo+"'");
            JOptionPane.showMessageDialog(null, "Result added successfull");
            setVisible(false);
            UpdateResult frame=new UpdateResult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

