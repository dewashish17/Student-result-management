    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        String  rollNO=rollno_textField.getText();
        try{
            Connection con =ConnectionProvider.getcon();
            Statement st=con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs=st.executeQuery("select * from student where rollno='"+rollNO+"'");
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered Roll no doesn't exist");
//                setVisible(false);
//                insertResult frame=new insertResult();
//                frame.setVisible(true);
            }
            else rollno_textField.setEditable(false);
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
    }//GEN-LAST:event_searchButtonActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String rollNo=rollno_textField.getText();
        int physicsMarks=Integer.parseInt(jTextField2.getText());
        int mathsMarks=Integer.parseInt(jTextField3.getText());
        int chemMarks=Integer.parseInt(jTextField4.getText());
        int electricalMarks=Integer.parseInt(jTextField5.getText());
        int bioMarks=Integer.parseInt(jTextField6.getText());
        int total=physicsMarks+mathsMarks+chemMarks+electricalMarks+bioMarks;
        
        try{
            Connection con=ConnectionProvider.getcon();
            Statement st=con.createStatement();     // using this we will execute command into sql
            st.executeUpdate("insert into result values(' "+rollNo+"','"+physicsMarks+"','"+mathsMarks+"','"+chemMarks+"','"+electricalMarks+"','"+bioMarks+"','"+total+"')");
            JOptionPane.showMessageDialog(null, "Result added successfull");
            setVisible(false);
            insertResult frame=new insertResult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
                
    }//GEN-LAST:event_jButton2ActionPerformed

